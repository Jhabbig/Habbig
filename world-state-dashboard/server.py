@app.get("/api/relations/{country_code}")
async def get_relations(country_code: str):
    code = country_code.upper()
    if code in COUNTRY_RELATIONS:
        return _json(COUNTRY_RELATIONS[code])
    return _json({"error": "Country not found"})


