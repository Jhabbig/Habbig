# ── Gate bypass ────────────────────────────────────────────────────────────

# Fixed token so the gate password is predictable across tests. Real env
# has a 32-char secret; tests only need something the middleware accepts.
E2E_GATE_TOKEN = "e2e-gate-token-deterministic"
_os.environ["SITE_ACCESS_TOKEN"] = E2E_GATE_TOKEN
# Prevent the middleware from ALSO requiring hardened prod-only flags.
_os.environ.pop("PRODUCTION", None)


@pytest.fixture
def gate_token() -> str:
    return E2E_GATE_TOKEN


@pytest.fixture
def pass_gate(client, gate_token):
    """Walk a client past the site gate. Returns the same client with
    the gate cookie set so follow-up navigation can proceed.

    Always clears the shared rate-limit store at call time — the gate
    is rate-limited per-IP, and TestClient re-uses 127.0.0.1 for every
    request so back-to-back e2e flows trip the limiter otherwise.
    """
    def _pass():
        try:
            import server as _server
            if hasattr(_server, "_rate_store"):
                _server._rate_store.clear()
            if hasattr(_server, "_login_failures"):
                _server._login_failures.clear()
        except Exception:
            pass
        # Double-submit CSRF: the cookie value and the submitted value
        # must match. Reads whatever token the server already baked into
        # the jar (first-visit GETs get one auto-generated by the
        # middleware) and echoes it back in the form body. If the jar is
        # empty we drop in a deterministic "t" ourselves.
        csrf_val = client.cookies.get("_csrf")
        if not csrf_val:
            csrf_val = "t"
            client.cookies.set("_csrf", csrf_val)
        # The gate handler accepts the form field `token` (see server.py).
        # Form-encoded POSTs read the CSRF token from `_csrf` in the body
        # (server.py::CSRFMiddleware parses the body for the form field
        # rather than consulting the header). JSON POSTs use the
        # x-csrf-token header; forms use the `_csrf` body field.
        r = client.post(
            "/gate",
            data={"token": gate_token, "_csrf": csrf_val},
            follow_redirects=False,
        )
        # 302/303 on success, 200 on invalid (renders the form again).
        assert r.status_code in (302, 303, 200), (
            f"gate bypass failed: {r.status_code} {r.text[:200]}"
        )
        # Some builds set the gate cookie via the middleware chain that the
        # TestClient consumes into .cookies automatically. If the cookie
        # didn't land, inject it directly so downstream requests still
        # look past-gate.
        if "narve_gate_access" not in client.cookies:
            client.cookies.set("narve_gate_access", gate_token)
        return client
    return _pass


