@pytest.fixture
def mock_stripe_webhook(stripe_webhook_secret):
    """Build (body, headers) for a signed Stripe event. Matches
    helpers.signed_stripe_event() shape but lets flow tests choose the
    event type + payload inline."""
    def _build(event_type: str, data_object: dict,
               *, event_id: Optional[str] = None) -> tuple[str, dict]:
        ev = {
            "id": event_id or f"evt_e2e_{secrets.token_hex(8)}",
            "type": event_type,
            "data": {"object": data_object},
            "created": int(_time.time()),
        }
        body = _json.dumps(ev, separators=(",", ":"))
        ts = str(int(_time.time()))
        sig_payload = f"{ts}.{body}".encode()
        v1 = _hmac.new(
            stripe_webhook_secret.encode(),
            sig_payload,
            _hashlib.sha256,
        ).hexdigest()
        return body, {
            "Stripe-Signature": f"t={ts},v1={v1}",
            "Content-Type": "application/json",
        }
    return _build


# ── Clock control — cheap freezegun alternative ────────────────────────────


@contextmanager
def _patch_time(monkeypatch, ts: int):
    """Patch time.time() and db.int(time.time())-style callers so
    follow-up code thinks `ts` seconds have elapsed since the last
    time.time() read. Does not alter datetime — flows that rely on
    datetime.utcnow need freezegun proper."""
    real = _time.time

    def fake() -> float:
        return float(ts)

    monkeypatch.setattr(_time, "time", fake)
    try:
        yield fake
    finally:
        monkeypatch.setattr(_time, "time", real)


@pytest.fixture
def fast_forward(monkeypatch):
    """Use as::

        with fast_forward(hours=25):
            # server now thinks we're 25 hours in the future
            ...
    """
    @contextmanager
    def _ff(*, seconds: int = 0, minutes: int = 0, hours: int = 0, days: int = 0):
        delta = seconds + 60 * minutes + 3600 * hours + 86400 * days
        target = int(_time.time()) + delta
        with _patch_time(monkeypatch, target):
            yield target
    return _ff


